**써보니 편한 Typescript**
<p>간단히 말해 타입스크립트는 자바스크립트에 변수 등에 형을 지정하는 것이다.</p>
<p>강의를 들으면서 확실히 타입스크립트가 편하다는 생각이 들었다.</p>
<p>타입스크립트의 ****장점</p>
<ul>
<li>VSCode 와 연동하여 타입을 지정해두면 자동완성을 지원함.</li>
