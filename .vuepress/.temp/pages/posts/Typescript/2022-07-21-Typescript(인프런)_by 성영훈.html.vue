</ul>
<p>강의에서는 타입스크립트의 이론 뿐 아니라, Javascript로 작성된 프로젝트를 Typescript로 변환해주는 과정을 진행하면서 “Typing” 하는 방법까지 배울 수 있어서 사용법을 익히는데 도움이 된다.</p>
